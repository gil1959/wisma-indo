<h2>Ringkasan</h2>
<p>Kami menghargai privasi Anda. Dokumen ini menjelaskan bagaimana informasi dikumpulkan, digunakan, dan dilindungi.</p>

<h2>Data yang kami kumpulkan</h2>
<ul>
  <li>Identitas dasar: nama, email, nomor telepon.</li>
  <li>Data transaksi: detail pemesanan, metode pembayaran, dan status pembayaran.</li>
  <li>Data teknis: alamat IP, browser, dan log aktivitas untuk keamanan.</li>
</ul>

<h2>Penggunaan data</h2>
<ul>
  <li>Memproses pemesanan dan layanan perjalanan.</li>
  <li>Menghubungi Anda terkait pesanan, perubahan jadwal, atau konfirmasi.</li>
  <li>Peningkatan layanan, analitik internal, dan keamanan.</li>
</ul>

<h2>Berbagi data</h2>
<p>Kami dapat membagikan data seperlunya kepada mitra terkait layanan (misalnya penyedia pembayaran dan operator tur) hanya untuk tujuan pemrosesan layanan.</p>

<h2>Kontak</h2>
<p>Jika Anda memiliki pertanyaan terkait privasi, silakan hubungi kami melalui halaman Contact.</p>
